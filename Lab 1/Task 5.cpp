// Task.5
// Write a C++ program to declare two integers, one float 
// variables and assign 10, 15, and 12.6 to them respectively. It then 
// prints these values on the screen.

#include <iostream>
using namespace std;

int main() {
	int x;
	int y;
	float z;
	
	x = 10;
	y = 15;
	z = 12.6;
	
	cout << x << endl;
	cout << y << endl;
	cout << z << endl;
	
	return 0;
}
